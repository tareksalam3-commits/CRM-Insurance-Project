import { UserRole, getRoleLevel } from './supabase';

type RoleGroup = 'management' | 'team_lead' | 'agent';
type MonthPhase = 'start' | 'mid' | 'end';

function getRoleGroup(role: UserRole): RoleGroup {
  const level = getRoleLevel(role);
  if (level <= 3) return 'management';
  if (level <= 5) return 'team_lead';
  return 'agent';
}

function getMonthPhase(date: Date): MonthPhase {
  const day = date.getDate();
  const daysInMonth = new Date(date.getFullYear(), date.getMonth() + 1, 0).getDate();
  const midCutoff = Math.round(daysInMonth * 0.35);
  const endCutoff = Math.round(daysInMonth * 0.75);

  if (day <= midCutoff) return 'start';
  if (day <= endCutoff) return 'mid';
  return 'end';
}

function getTimeBucketIndex(hour: number): number {
  if (hour >= 5 && hour < 12) return 0;
  if (hour >= 12 && hour < 17) return 1;
  if (hour >= 17 && hour < 22) return 2;
  return 3;
}

