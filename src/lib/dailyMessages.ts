import { UserRole, getRoleLevel } from './supabase';

type RoleGroup = 'management' | 'team_lead' | 'agent';
type MonthPhase = 'start' | 'mid' | 'end';

// تحديد فئة الدور: إدارة عليا / قائد فريق / وكيل ميداني
function getRoleGroup(role: UserRole): RoleGroup {
  const level = getRoleLevel(role);
  if (level <= 3) return 'management'; // super_admin, development_manager, general_supervisor
  if (level <= 5) return 'team_lead';  // supervisor, group_leader
  return 'agent';                      // agent, premium_agent
}

// تحديد مرحلة الشهر: بداية / منتصف / نهاية (حسب عدد أيام الشهر الفعلي)
function getMonthPhase(date: Date): MonthPhase {
  const day = date.getDate();
  const daysInMonth = new Date(date.getFullYear(), date.getMonth() + 1, 0).getDate();
  const midCutoff = Math.round(daysInMonth * 0.35);   // تقريبًا يوم 10-11
  const endCutoff = Math.round(daysInMonth * 0.75);   // تقريبًا يوم 22-23

  if (day <= midCutoff) return 'start';
  if (day <= endCutoff) return 'mid';
  return 'end';
}

// فهرس وقت اليوم (0 صباح، 1 ظهر، 2 مساء، 3 ليل) - بيتحكم في تنويع الرسالة خلال اليوم
function getTimeBucketIndex(hour: number): number {
  if (hour >= 5 && hour < 12) return 0;
  if (hour >= 12 && hour < 17) return 1;
  if (hour >= 17 && hour < 22) return 2;
  return 3;
}

