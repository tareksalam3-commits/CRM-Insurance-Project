/**
 * بيرجع رسالة اليوم المناسبة حسب الدرجة الوظيفية، مرحلة الشهر، ووقت اليوم.
 * الرسالة بتتغير تلقائيًا كل يوم وكل فترة من اليوم، من غير عشوائية بحتة
 * (نفس المدخلات = نفس الرسالة، عشان متتغيرش مع كل إعادة تحميل لنفس اللحظة)
 */
export function getDailyMessage(role: UserRole, now: Date = new Date()): string {
  const roleGroup = getRoleGroup(role);
  const monthPhase = getMonthPhase(now);
  const timeBucket = getTimeBucketIndex(now.getHours());

  const pool = MESSAGES[`${roleGroup}_${monthPhase}`];
  const index = (now.getDate() * 4 + timeBucket) % pool.length;

  return pool[index];
}
